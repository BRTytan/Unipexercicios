"""exercicio 05: Escreva o programa que calcule a soma de três variaveis e imprima o resultado"""

num1 = int(input('Digite um numero: '))
num2 = int(input('Digite outro numero: '))
num3 = int(input('Digite outro numero: '))
resultado = int(num1 + num2 + num3)
print('A soma entre os numeros digitados é: {}'.format(resultado))